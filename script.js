// while loop

document.write("Hello");

for (var dog = 0; dog < 10; dog++) {
    document.write("<br>");
    document.write(dog);
}


var cat = 0; 
do {
    document.write("<br>");
    document.write(cat);
    cat++;
} while (cat < 10);  



var monkey = 0;
while (monkey < 10) {
    document.write("<br>");
    document.write(monkey);
    monkey++;
}

